package com.board.beans;

public class Applicant extends User{
	private String displaySchool;
	private String displayDegree;
	private String displayOrgName;
	private String displayRole;
	
	public String getDisplaySchool() {
		return displaySchool;
	}
	public void setDisplaySchool(
			String displaySchool) {
		this.displaySchool = displaySchool;
	}
	public String getDisplayDegree() {
		return displayDegree;
	}
	public void setDisplayDegree(
			String displayDegree) {
		this.displayDegree = displayDegree;
	}
	public String getDisplayOrgName() {
		return displayOrgName;
	}
	public void setDisplayOrgName(
			String displayOrgName) {
		this.displayOrgName = displayOrgName;
	}
	public String getDisplayRole() {
		return displayRole;
	}
	public void setDisplayRole(
			String displayRole) {
		this.displayRole = displayRole;
	}
	
	
}
